    private void jBBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarClienteActionPerformed

        if (!RepositorioClientes.listaClientes.isEmpty()) {

            String cpf = jTCpf.getText();

            if (!"   .   .   -  ".equals(cpf)) {
                Cliente _c = RepositorioClientes.pesquisarClienteCpf(cpf);

                if (_c != null) {
                    //passa os dados do cliente para a global 
                    this.c = _c;
                    jTNomeCliente.setText(_c.getNome());

                } else {
                    JOptionPane.showMessageDialog(this, "Cliente não cadastrado");
                    jTCpf.setText(null);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Informe o cpf do cliente!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "O sistema não possui clientes cadastrados!");
        }

    }//GEN-LAST:event_jBBuscarClienteActionPerformed

    private void jBBuscarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarProdutoActionPerformed

        if (!"".equals(jTCodigoProduto.getText())) {

            int codProduto = Integer.parseInt(jTCodigoProduto.getText());

            if (codProduto != 0) {
                Produto _p = RepositorioProdutos.pesquisarProdutoCod(codProduto);

                if (_p != null) {
                    //passa os dados do produto para a global 
                    this.p = _p;
                    //seta os campos 
                    jTNomeProduto.setText(_p.getNome());
                    jTValorUnit.setText(String.valueOf(_p.getPrecoVenda()));
                } else {
                    JOptionPane.showMessageDialog(this, "Produto não cadastrado");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Informe o código do produto");
        }
    }//GEN-LAST:event_jBBuscarProdutoActionPerformed

