    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed

        if (validarCampos() == true) {

            if (!"".equals(jTDesconto.getText()) && !"".equals(jTValorTotal.getText())) {

                int opcao = JOptionPane.showConfirmDialog(this, "\nNome cliente: " + c.getNome() + "\nNome vendedor: " + f.getNome()+ "\nForma de pagamento: "+jComboBox1.getSelectedItem() + "\nTotal: " + jTTotal.getText()+"\n", "Os dados estão corretos? ", JOptionPane.YES_NO_OPTION);

                if (opcao == JOptionPane.YES_OPTION) {

                    String nomeCliente = this.c.getNome();
                    String nomeVendedor = this.f.getNome();
                    //faz uma copia do arrayList contendo o pedido do cliente
                    ArrayList<Pedido> pedido = new ArrayList(ListaPedidos.carrinhoCompras);
                    String dataCompra = getData();
                    String formaPagamento = jComboBox1.getSelectedItem() + "";
                    double desconto = Double.parseDouble(jTDesconto.getText());
                    double total = Double.parseDouble(jTTotal.getText());

                    //criar um objeto venda 
                    Venda v = new Venda(dataCompra, formaPagamento, total, desconto, nomeCliente, nomeVendedor, pedido);

                    //adiciona uma venda ao ArrayList 
                    RepositorioVendas.addVenda(v);

                    //mensagem confirmacao, falta editar
                    JOptionPane.showMessageDialog(this, "Venda concluída!");

                    //apagando o arrayList inteiro para receber pedidos de outros clientes 
                    ListaPedidos.carrinhoCompras.clear();

                    //teste
                    System.out.print(ListaPedidos.carrinhoCompras.size());

                    //limpar campos 
                    limparCampos();

                    //atualizar estoque dos produtos comprados 
                    RepositorioProdutos.atualizarEstoque(pedido);

                    //fechar a tela 
                    this.dispose();

                } else if (opcao == JOptionPane.NO_OPTION) {

                    JOptionPane.showMessageDialog(this, "Venda cancelada");

                    limparCampos();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Escolha uma forma de pagamento");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Campo(s) em branco!");
        }

    }//GEN-LAST:event_jButton9ActionPerformed

