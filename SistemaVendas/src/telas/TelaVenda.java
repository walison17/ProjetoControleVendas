    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Pedido _pedido = carrinhoTabela.getPedido(jTablePedido.getSelectedRow());

        //se nenhuma linha da tabela estiver selecionada (getSelectedRow = -1) o Pedido retorna vazio
        if (_pedido != null) {
            ListaPedidos.removerPedido(_pedido);
            carrinhoTabela.atualizarTabela();

            //atualizando campos da interface
            String itens = Double.parseDouble(ListaPedidos.carrinhoCompras.size() + "") + ""; //gambiarra ON! 
            jTItens.setText(itens);

            //coleta o valor atual da variavel e calcula o pedido retirado
            String qT = Double.parseDouble(jTQuantTotal.getText()) - _pedido.getQuantProdutos() + "";
            jTQuantTotal.setText(qT);

            String total = Double.parseDouble(jTTotal.getText()) - _pedido.getSubTotal() + "";
            jTTotal.setText(total);

        } else {
            JOptionPane.showMessageDialog(this, "Selecione na lista o produto que deseja remover");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

