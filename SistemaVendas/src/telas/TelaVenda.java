    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //logica para realizar o pedido e exibir os dados na tabela 
        if (jTNomeProduto.getText() != null) {

            if (!"".equals(jTQuantidadeProdutos.getText())) {

                //coletando e armazenando dados 
                String nomeProduto = this.p.getNome();
                int codProduto = this.p.getCodigo();
                double precoProduto = this.p.getPrecoVenda();
                double quantProdutos = Double.parseDouble(jTQuantidadeProdutos.getText());
                double subTotal = (quantProdutos * precoProduto);

                //criando um pedido
                Pedido _pedido = new Pedido(codProduto, nomeProduto, quantProdutos, precoProduto, subTotal);
                carrinhoTabela.atualizarTabela();

                //adiciona o Pedido no ArrayList
                ListaPedidos.addPedido(_pedido);

                if (quantProdutos <= this.p.getQuantidade() && ListaPedidos.quantTotal() <= this.p.getQuantidade()) {

                    //itens
                    String itens = Double.parseDouble(ListaPedidos.carrinhoCompras.size() + "") + "";  //gambiarra huehuehuehu
                    jTItens.setText(itens);

                    //quantidadeTotal
                    String quantTotal = ListaPedidos.quantTotal() + "";
                    jTQuantTotal.setText(quantTotal);

                    //Total
                    String total = ListaPedidos.valorTotal() + "";
                    jTTotal.setText(total);

                    //limpar campos
                    jTNomeProduto.setText(null);
                    jTQuantidadeProdutos.setText(null);
                    jTCodigoProduto.setText(null);
                    jTValorUnit.setText(null);

                    //teste
                    System.out.print(p);
                } else {

                    JOptionPane.showMessageDialog(this, "O produto não possui estoque suficiente para atender o pedido");
                    ListaPedidos.carrinhoCompras.remove(_pedido);
                    carrinhoTabela.atualizarTabela();
                }
            } else if ("".equals(jTQuantidadeProdutos.getText())) {
                JOptionPane.showMessageDialog(this, "Informe a quantidade");
            }
        } else if ("".equals(jTCodigoProduto.getText())) {
            JOptionPane.showMessageDialog(this, "Informe o código do produto");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

